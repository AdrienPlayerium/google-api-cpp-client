#ifndef UTIL_MEMORY_FREE_DELETER_H_
#define UTIL_MEMORY_FREE_DELETER_H_

#include <stdlib.h>
namespace googleapis {

namespace util {
namespace memory {

// Function object which invokes 'free' on its parameter, which must be
// a pointer. Can be used to store malloc-allocated pointers in std::unique_ptr:
//
// std::unique_ptr<int, util::memory::FreeDeleter> foo_ptr(
//     static_cast<int*>(malloc(sizeof(int))));
struct FreeDeleter {
  inline void operator()(void* ptr) const {
    free(ptr);
  }
};

}  // namespace memory
}  // namespace util

}  // namespace googleapis
#endif  // UTIL_MEMORY_FREE_DELETER_H_
